#include "3-calc.h"

/**
 * op_add - adds a and b
 * @a: int a
 * @b: int b
 * Return: returns int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - adds a and b
 * @a: int a
 * @b: int b
 * Return: returns int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds a and b
 * @a: int a
 * @b: int b
 * Return: returns int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - adds a and b
 * @a: int a
 * @b: int b
 * Return: returns int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds a and b
 * @a: int a
 * @b: int b
 * Return: returns int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
